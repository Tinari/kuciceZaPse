    private void popuniCmbTipovi() {
        try {
            cmbTipovi.removeAllItems();
            List<TipKuciceZaPse> lista = Controler.getInstance().vratiListuTipova();
            for (IDomain tip : lista) {
                if (tip instanceof TipKuciceZaPse) {
                    cmbTipovi.addItem((TipKuciceZaPse) tip);
                }
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne moze da ucita listu tipova kucica za pse");
            Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne moze da ucita listu tipova kucica za pse");
            Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void prepareForm(FormMode mode) {
        switch (mode) {
            case EDIT:
                btnIzmeni.setEnabled(true);

                btnPokreniIzmenu.setEnabled(false);
                btnSacuvaj.setEnabled(false);
                btnIzmeni.setEnabled(true);
                btnPretaga.setEnabled(true);

                txtNaziv.setVisible(true);

                cmbTipovi.setEnabled(true);
                txtNaziv.setEnabled(true);

                break;
            case NEW:
                btnIzmeni.setEnabled(false);

                btnPokreniIzmenu.setEnabled(false);
                btnSacuvaj.setEnabled(true);
                btnOtkazi.setEnabled(true);
                btnPretaga.setEnabled(true);

                txtNaziv.setVisible(true);
                txtNaziv.setEnabled(true);
                cmbTipovi.setEnabled(true);
                break;
            case VIEW:
                btnIzmeni.setEnabled(false);

                btnPokreniIzmenu.setEnabled(true);
                btnSacuvaj.setEnabled(false);
                btnOtkazi.setEnabled(false);
                btnPretaga.setEnabled(false);

                txtNaziv.setVisible(true);
                txtNaziv.setEnabled(false);

                cmbTipovi.setEnabled(false);
                break;
        }
    }

    private void pripremiFomuZaPromene(int idKucicaZaPse) {
        prepareForm(FormMode.VIEW);

        try {
            kucicaRef = Controler.getInstance().vratiKucicuZaPsePoId(idKucicaZaPse);
            setTitle("Pregled kucice za pse " + kucicaRef.getNazivKuciceZaPse());
            txtNaziv.setText(kucicaRef.getNazivKuciceZaPse());
            cmbTipovi.setSelectedItem(kucicaRef.getTipKuciceZaPse());
            List<NacinIzrade> nacini = Controler.getInstance().vratiNacineZaKuciceZaPse(kucicaRef);
            TableModelNaciniIzrade model = new TableModelNaciniIzrade(nacini);
            tblNacini.setModel(model);
            jButton1.setVisible(true);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne moze da nadje proizvod po zadatkom id!!");
            Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne moze da nadje proizvod po zadatkom id!!");
            Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
