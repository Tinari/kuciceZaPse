    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        try {
            String naziv = txtNaziv.getText().trim();
            TipKuciceZaPse tip = (TipKuciceZaPse) cmbTipovi.getSelectedItem();
            KucicaZaPse kzp = new KucicaZaPse(naziv, tip);
            if (naziv.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Morate uneti sva polja!");
            }
            System.out.println("ovo je kucica za pse " + kzp.getNazivKuciceZaPse());
            kucicaRef = Controler.getInstance().zapamtiKucicuZaPse(kzp);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio kucicu za pse!");

            int result = JOptionPane.showConfirmDialog(null, "Da li zelite uneti jos neku kucicu za pse?");
            if (result == JOptionPane.YES_OPTION) {
                this.dispose();
                JDialog frm = new FrmKucicaZaPse(null, true);
                frm.setVisible(true);
            } else {
                dispose();
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne moze da zapamti kucicu za pse!");
            Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne moze da zapamti kucicu za pse!");
            Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        String naziv = txtNaziv.getText().trim();
        TipKuciceZaPse tip = (TipKuciceZaPse) cmbTipovi.getSelectedItem();
        KucicaZaPse kzp = new KucicaZaPse(kucicaRef.getKucicaZaPseId(), naziv, tip);

        if (naziv.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Morate uneti sva polja!!!");
            return;
        }
        try {
            kzp = Controler.getInstance().izmeniKucicuZaPse(kzp);
            JOptionPane.showMessageDialog(null, "Sistem je azurirao kucicu za pse!");
            JOptionPane.showMessageDialog(null, "Sistem ne moze da izmeni kucicu za pse!");
            dispose();
            frmPretraga.popuniTabeluKucicaZaPse();
            frmPretraga.pripremiZaPretragu();
        } catch (ClassNotFoundException ex) {
            //Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Sistem ne moze da izmeni kucicu za pse!");
        } catch (Exception ex) {
            //Logger.getLogger(FrmKucicaZaPse.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Sistem ne moze da izmeni kucicu za pse!");
        }


    }//GEN-LAST:event_btnIzmeniActionPerformed

